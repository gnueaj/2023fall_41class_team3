import React, { useEffect, useRef } from "react";
import { monaco } from "react-monaco-editor";

const RefactoringDiff = () => {
  const containerRef = useRef(null); // 컨테이너에 대한 ref 생성

  useEffect(() => {
    if (containerRef.current) {
      const originalModel = monaco.editor.createModel(
        "hello world",
        "text/plain"
      );
      const modifiedModel = monaco.editor.createModel(
        "Hello World!",
        "text/plain"
      );

      const diffEditor = monaco.editor.createDiffEditor(containerRef.current, {
        originalEditable: true,
        automaticLayout: true,
      });

      diffEditor.setModel({
        original: originalModel,
        modified: modifiedModel,
      });

      return () => diffEditor.dispose(); // 컴포넌트가 언마운트될 때 에디터 정리
    }
  }, []);
  return (
    <div className="h-full ">
      <div className="h-[90%] bg-surface-dark rounded-xl">
        <div className="h-[90%] py-10 flex-center overflow-hidden ">
          <div
            id="container"
            // style={{ height: "90%" }}
            ref={containerRef}
            className="w-[90%] h-full m-auto">
            <div className="flex-col py-4 text-xl font-bold border flex-center rounded-t-xl bg-background">
              Suggested Change
              <div className="flex p-2 text-sm text-red-400">
                Refactored code should always be reviewed by a person. Code
                generated by AI may contain bugs or behave unexpectedly.
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
};

export default RefactoringDiff;
